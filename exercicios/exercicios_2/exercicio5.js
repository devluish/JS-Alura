/*Crie uma variável com uma string e utilize métodos de manipulação de strings, como toUpperCase, toLowerCase, slice ou outros, para modificar a string original. 
Exiba os resultados finais no console. */

const nomeCompleto = "Luis Henrique Oliveira";

console.log(nomeCompleto.toUpperCase()); //Retorna em caixa alta
console.log(nomeCompleto.toLowerCase()); //Retorna em caixa baixa
console.log(nomeCompleto.slice()); //Retorna as letras após o espaço em caixa alta Luis Henrique Oliveira

